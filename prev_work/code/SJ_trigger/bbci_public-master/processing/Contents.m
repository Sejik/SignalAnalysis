% This directory contains the files which can be used for various ways for
% data processing (offline and online) and analyses.
%
%PROC_AVERAGE - Classwise calculated averages
%PROC_FLATEN - reduce dimensionality of a data struct by stacking
%PROC_LOGARITHM - computes the natural logarithm
%PROC_NORMALIZE - normalize to e.g. unit variance,
%PROC_VARIANCE - computes the variance in equally spaced intervals
%
%
%PROC_APPENDCHANNELS - Append a channel to continuous or epoched data
%PROC_APPENDCNT - Append Continuous EEG Data
%PROC_APPENDEPOCHS - appends two or more epochs.
%PROC_APPENDSAMPLES - append samples of data structs
%PROC_BASELINE - substract a baseline from a epoched data structure
%PROC_SUBTRACTREFERENCECLASS - subtracts a given the reference class
%PROC_REJECTARTIFACTSMAXMIN - Reject epochs according to max-min criterion
%PROC_SEGMENTATION - Segment continuous signals into epochs
%PROC_SELECTCHANNELS - Select channels from continuous or epoched data
%PROC_SELECTCLASSES - select only a subset of the avialble classes
%PROC_SELECTEPOCHS - Select specified epochs within epoch structure
%PROC_SELECTIVAL - Select subinterval from epoched data
%PROC_SELECTSAMPLES - Select a subset of samples of a data set
%PROC_COMBINECLASSES - combine/merge several Classes into one
%
%
%PROC_AUCVALUES - Computes the area under the curve (AUC) score for each feature
%PROC_RSQUARE - computes r^2 values (measure for discriminance)
%PROC_RSQUARESIGNED - computes signed r^2 values (measure for discriminance)
%
%
%PROC_ENVELOPE - Envelope Curve of Oscillatory Signals
%PROC_FILT - Digital filtering
%PROC_FILTFILT - Zero-phase forward and reverse digital filtering
%PROC_CSPAUTO - Common Spatial Pattern Analysis with Auto Filter Selection
%PROC_SPECTRUM -  calculate the power spectrum
