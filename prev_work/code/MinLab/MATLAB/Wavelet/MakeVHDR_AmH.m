	if isempty(strfind(fname, '.dat')), return; end;% *.dat 확장자에 대해서만!
	if nargin<2, SpI		=	2000; end;			% 비어 있으면 default로.
	if nargin<3, chanlist	=	chan32; end;		% 비어 있으면 default로.

	% VHDR 파일을 생성한다.
	fvhdr	=	[ fname(1:strfind(fname, '.dat')) 'vhdr' ];	%파일 path
	fVHDR	=	fopen(fvhdr, 'w');
	fprintf('Generating VHDR file to %s\n', fvhdr);

	%저장될 데이터를 구성함.
	vhdr	=	strsplit(fname, '/');						%path에 대한 토큰분리
	vhdr	=	vhdr(end);									%파일명만 추출
%	vhdr	=	strrep(vhdr, '.m', '.dat');					%확장자 변경	%-[
	chanstr	=	'';
	for c = 1 : length(chanlist)							%채널이름 문자열 구성
		chanstr	=	[ chanstr 'Ch' num2str(c) '=' chanlist(c) ',,,쨉V\n' ];
	end
	charstr	=	cell2mat(chanstr);

	header	=	['Brain Vision Data Exchange Header File Version 1.0\n'		...
'\n'																		...
'[Common Infos]\n'															...
'Codepage=UTF-8\n'															...
'DataFile=' char(vhdr) '\n'														...
'\n'																		...
'DataFormat=ASCII\n'														...
'; Data orientation: VECTORIZED=ch1,pt1, ch1,pt2..., '						...
	'MULTIPLEXED=ch1,pt1, ch2,pt1 ...\n'									...
'DataOrientation=MULTIPLEXED\n'												...
'DataType=TIMEDOMAIN\n'														...
'NumberOfChannels=' char(num2str(length(chanlist))) '\n'					...
'; Sampling interval in microseconds if time domain (convert to Hertz:\n'	...
'; 1000000 / SamplingInterval) or in Hertz if frequency domain:\n'			...
'SamplingInterval=' num2str(SpI) '\n'										...
'Averaged=NO\n'																...
'SegmentDataPoints=0\n'														...
'SegmentationType=NOTSEGMENTED\n'											...
'DataPoints=0\n'															...
'\n'																		...
'[ASCII Infos]\n'															...
'; Decimal symbol for floating point numbers: '								...
	'the header file always uses a dot (.),\n'								...
'; however the data file might use a different one\n'						...
'DecimalSymbol=.\n'															...
'; SkipLines, SkipColumns: leading lines and columns with additional '		...
	'informations.\n'														...
'SkipLines=0\n'																...
'SkipColumns=0\n'															...
'\n'																		...
'[Channel Infos]\n'															...
'; Each entry: Ch<Channel number>=<Name>,<Reference channel name>,\n'		...
'; <Resolution in "Unit">,<Unit>,<Future extentions...\n'					...
'; Fields are delimited by commas, some fields might be omitted (empty).\n'	...
'; Commas in channel names are coded as "\1".\n'							...
char(cell2mat(chanstr))														...
'\n'																		...
'[Channel User Infos]\n'													...
'; Each entry: Prop<Number>=Ch<ChannelNumber>,<Type>,<Name>,<Value>,'		...
	'<Value2>,...,<ValueN>\n'												...
'; Property number must be unique. Types can be int, single, string, '		...
	'bool, byte, double, uint\n'											...
'; or arrays of those, indicated int-array etc\n'							...
'; Array types have more than one value, number of values determines size '	...
	'of array.\n'															...
'; Fields are delimited by commas, commas in strings are written \1\n'		...
'; Properties are assigned to channels using their channel number.\n'		...
'\n'																		...
'[Coordinates]\n'															...
'; Each entry: Ch<Channel number>=<Radius>,<Theta>,<Phi>\n'					...
char(GetChanLoc(length(chanlist)))	];	%-]

	fprintf(fVHDR, header);
	fclose(fVHDR);

